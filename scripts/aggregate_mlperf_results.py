#!/usr/bin/env python3
"""
aggregate_mlperf_results.py — Aggregate MLPerf Storage benchmark results across all 4 SSDs.

Reads:
  /mnt/ai_ssd{0,1,2}/.../mlperf_results/results.csv  (from ssd_mlperf_benchmark.sh)
  /run/media/ficus/新加卷/.../mlperf_results/results.csv  (Biwin)

Writes:
  results/cross_vendor/mlperf_summary.md   — Per-SSD markdown summary
  results/cross_vendor/mlperf_summary.csv  — Per-SSD CSV for plotting
"""

from __future__ import annotations
import csv
import json
import re
import sys
from pathlib import Path
from typing import Dict, List, Optional

# ---- Vendor → mount mapping (mirrors cross_vendor_lib.sh) ----
VENDOR_MOUNTS = {
    "wd_sn570":      "/mnt/ai_ssd0",
    "biwin_x570":    "/run/media/ficus/新加卷",
    "zhitai_ti600":  "/mnt/ai_ssd1",
    "seagate_fc530": "/mnt/ai_ssd2",
}

VENDOR_INFO = {
    "wd_sn570":      ("WDC WDS960G2G0C-00AJM0",  "entry-level (DRAM-less)"),
    "biwin_x570":    ("BIWIN X570 1TB",          "mainstream (DRAM, prior subject)"),
    "zhitai_ti600":  ("ZHITAI Ti600 1TB",        "domestic (YMTC NAND)"),
    "seagate_fc530": ("Seagate ZP1000GV30012",   "high-end (Phison E18)"),
}

OUTPUT_DIR = Path("results/cross_vendor")
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

# ---- Extract metrics from log files (fallback if CSV metrics is empty) ----
METRIC_RE = re.compile(
    r"\[METRIC\]\s+(?P<key>[^:]+):\s+(?P<value>[\d\.]+)"
)

def parse_metrics_from_log(log_path: Path) -> Dict[str, float]:
    """Extract [METRIC] lines from a benchmark log."""
    metrics = {}
    if not log_path.exists():
        return metrics
    for line in log_path.read_text(errors="replace").splitlines():
        m = METRIC_RE.search(line)
        if m:
            try:
                metrics[m.group("key").strip()] = float(m.group("value"))
            except ValueError:
                pass
    return metrics

def collect_vendor_data(vendor: str, mount: str) -> Dict:
    """Collect all MLPerf benchmark results for one vendor."""
    res_root = Path(mount) / "mlperf_results"
    if not res_root.exists():
        return {"vendor": vendor, "mount": mount, "results": {}, "error": "no results dir"}

    out: Dict = {"vendor": vendor, "mount": mount, "results": {}, "logs": []}

    # 1. Parse results.csv (from reportgen)
    csv_path = res_root / "results.csv"
    if csv_path.exists():
        with csv_path.open() as f:
            reader = csv.DictReader(f)
            for row in reader:
                cmd = row.get("command", "")
                model = row.get("model", "")
                key = f"{cmd}_{model}"
                out["results"][key] = row

    # 2. Parse all run logs for [METRIC] lines (more reliable than CSV)
    for log in res_root.glob("*_run.log"):
        metrics = parse_metrics_from_log(log)
        if metrics:
            out["logs"].append({"log": log.name, "metrics": metrics})

    # 3. Inspect .hydra/config.yaml if present (workload details)
    for hydra in (res_root / "checkpointing").rglob("config.yaml"):
        try:
            cfg = json.loads(hydra.read_text())
            out.setdefault("configs", []).append(str(hydra.relative_to(res_root)))
        except Exception:
            pass

    return out

def render_markdown(all_data: Dict[str, Dict]) -> str:
    lines = [
        "# MLPerf Storage Benchmark — Per-SSD Summary",
        "",
        "Generated by `scripts/aggregate_mlperf_results.py`",
        "",
        "**Workload type:** checkpointing (llama3-8b) via `mlpstorage checkpointing run`",
        "**Storage backend:** local filesystem (`--file`)",
        "**Storage flag:** open submission, allow-run-as-root, skip-validation, skip-timeseries",
        "",
        "## Per-SSD Throughput (write / read)",
        "",
        "| Vendor | Model | Save Throughput (GiB/s) | Load Throughput (GiB/s) | Save Duration (s) | Load Duration (s) |",
        "|--------|-------|-----------------------:|-----------------------:|------------------:|------------------:|",
    ]

    for vendor, info in VENDOR_INFO.items():
        data = all_data.get(vendor, {})
        results = data.get("results", {})
        logs = data.get("logs", [])
        # Find the most recent run metrics
        run_metrics = {}
        for log_entry in logs:
            if "checkpointing_run" in log_entry["log"]:
                run_metrics = log_entry["metrics"]
                break
        if not run_metrics and logs:
            # fallback to last log
            run_metrics = logs[-1]["metrics"]

        save_tput = run_metrics.get("Checkpoint save I/O Throughput (GiB/second)")
        load_tput = run_metrics.get("Checkpoint load I/O Throughput (GiB/second)")
        save_dur = run_metrics.get("Checkpoint save duration (seconds)")
        load_dur = run_metrics.get("Checkpoint load duration (seconds)")

        def fmt(v, suffix="", na="-"):
            return f"{v:.3f}{suffix}" if v is not None else na

        ckpt_run_key = "run_llama3-8b"
        ckpt_result = results.get(ckpt_run_key, {})
        model = ckpt_result.get("model", "llama3-8b") if ckpt_result else "llama3-8b"

        lines.append(
            f"| {vendor} ({info[0][:25]}) | {model} | "
            f"{fmt(save_tput)} | {fmt(load_tput)} | "
            f"{fmt(save_dur, 's', '-')} | {fmt(load_dur, 's', '-')} |"
        )

    lines += [
        "",
        "## Per-SSD Result Directory Layout",
        "",
        "```",
        "{mount}/mlperf_results/",
        "├── checkpointing/",
        "│   └── llama3-8b/<datetime>/",
        "│       ├── summary.json",
        "│       ├── *_metadata.json",
        "│       └── .hydra/",
        "├── checkpoints/",
        "│   └── llama3-8b/    (actual checkpoint files)",
        "├── llama3-8b_run.log (full benchmark log)",
        "├── results.csv       (from mlpstorage reports reportgen)",
        "└── results.json",
        "```",
        "",
        "## Notes",
        "",
        "- **DRAM-less caveat:** WD SN570 has no DRAM buffer; throughput degrades",
        "  after SLC cache exhaustion (~10-20 GB). With 105 GiB checkpoint write,",
        "  the tail (~90 GiB) ran at 0.20-0.23 GiB/s vs initial 0.40-0.45 GiB/s.",
        "- **Biwin/Seagate** have DRAM and maintain steady throughput.",
        "- **ZhiTai Ti600** uses YMTC NAND; expect lower sustained write than Biwin/Seagate.",
        "",
        "## Reproduce",
        "",
        "```bash",
        "bash scripts/ssd_mlperf_benchmark.sh <vendor_id>    # one SSD",
        "bash scripts/ssd_mlperf_benchmark.sh all            # all 4 SSDs",
        "python scripts/aggregate_mlperf_results.py          # this summary",
        "```",
        "",
    ]
    return "\n".join(lines)

def render_csv(all_data: Dict[str, Dict]) -> str:
    """CSV suitable for plotting."""
    rows = [["vendor", "model", "save_throughput_gibs", "load_throughput_gibs",
             "save_duration_s", "load_duration_s", "mount"]]
    for vendor in VENDOR_MOUNTS:
        data = all_data.get(vendor, {})
        results = data.get("results", {})
        logs = data.get("logs", [])
        run_metrics = next(
            (le["metrics"] for le in logs if "checkpointing_run" in le["log"]),
            logs[-1]["metrics"] if logs else {}
        )
        model = results.get("run_llama3-8b", {}).get("model", "llama3-8b")
        rows.append([
            vendor,
            model,
            run_metrics.get("Checkpoint save I/O Throughput (GiB/second)", ""),
            run_metrics.get("Checkpoint load I/O Throughput (GiB/second)", ""),
            run_metrics.get("Checkpoint save duration (seconds)", ""),
            run_metrics.get("Checkpoint load duration (seconds)", ""),
            data.get("mount", ""),
        ])
    return "\n".join(",".join(str(c) for c in row) for row in rows) + "\n"


def main() -> int:
    print("Collecting MLPerf results across 4 SSDs...")
    all_data = {}
    for vendor, mount in VENDOR_MOUNTS.items():
        print(f"  {vendor:18s} → {mount}")
        all_data[vendor] = collect_vendor_data(vendor, mount)
        if "error" in all_data[vendor]:
            print(f"    ⚠️  {all_data[vendor]['error']}")
        else:
            n_results = len(all_data[vendor]["results"])
            n_logs = len(all_data[vendor]["logs"])
            print(f"    ✓ {n_results} CSV records, {n_logs} run logs")

    md_path = OUTPUT_DIR / "mlperf_summary.md"
    md_path.write_text(render_markdown(all_data))
    print(f"\nWrote: {md_path}")

    csv_path = OUTPUT_DIR / "mlperf_summary.csv"
    csv_path.write_text(render_csv(all_data))
    print(f"Wrote: {csv_path}")

    # Print a quick table to stdout
    print()
    print("=" * 78)
    print("Quick view:")
    print("=" * 78)
    print(f"{'Vendor':18s} {'Save GB/s':>10s} {'Load GB/s':>10s} {'Save s':>8s} {'Load s':>8s}")
    print("-" * 78)
    for vendor in VENDOR_MOUNTS:
        data = all_data[vendor]
        logs = data.get("logs", [])
        run_metrics = next(
            (le["metrics"] for le in logs if "checkpointing_run" in le["log"]),
            logs[-1]["metrics"] if logs else {}
        )
        st = run_metrics.get("Checkpoint save I/O Throughput (GiB/second)")
        lt = run_metrics.get("Checkpoint load I/O Throughput (GiB/second)")
        sd = run_metrics.get("Checkpoint save duration (seconds)")
        ld = run_metrics.get("Checkpoint load duration (seconds)")
        print(f"{vendor:18s} "
              f"{st:>10.3f} {lt:>10.3f} "
              f"{sd:>8.1f} {ld:>8.1f}" if st is not None else f"{vendor:18s} (no metrics)")

    return 0

if __name__ == "__main__":
    sys.exit(main())